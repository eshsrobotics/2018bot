package org.usfirst.frc.team1759.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 * 
 * @author Aidan Galbreath
 */
public class RobotMap {
	public static int LEFT_FRONT = 5;
	public static int LEFT_MID = 2;
	public static int LEFT_BACK = 6;
	public static int RIGHT_FRONT = 0;
	public static int RIGHT_MID = 3;
	public static int RIGHT_BACK = 1;
	
	public static int HIGH_LAUNCH = 0;
	public static int LOW_LAUNCH = 1;
	
	public static int LEFT_IN = 7;
	public static int RIGHT_IN = 8;
	
	private RobotMap() {
		
	}
}

